"""ProgressiveTax"""
def tax(salary):
    """You can't escape the TAXES"""
    if salary <= 150000:
        paytax = 0
    elif salary <= 300000:
        paytax = (salary - 150000) * (5 / 100)
    elif salary <= 500000:
        paytax = (150000) * (5 / 100) + (salary - 300000) * (10 / 100)
    elif salary <= 750000:
        paytax = (150000) * (5 / 100) + (200000) * (10 / 100) + (salary - 500000) * (15 / 100)
    elif salary <= 1000000:
        paytax = (150000) * (5 / 100) + (200000) * (10 / 100) + (250000) * (15 / 100)\
+ (salary - 750000) * (20 / 100)
    elif salary <= 2000000:
        paytax = (150000) * (5 / 100) + (200000) * (10 / 100) + (250000) * (15 / 100)\
+ (250000) * (20 / 100) + (salary - 1000000) * (25 / 100)
    elif salary <= 4000000:
        paytax = (150000) * (5 / 100) + (200000) * (10 / 100) + (250000) * (15 / 100)\
+ (250000) * (20 / 100) + (1000000) * (25 / 100) + (salary - 2000000) * (30 / 100)
    elif salary > 4000000:
        paytax = (150000) * (5 / 100) + (200000) * (10 / 100) + (250000) * (15 / 100)\
+ (250000) * (20 / 100) + (1000000) * (25 / 100) + (2000000) * (30 / 100)\
+ (salary - 4000000) * (35 / 100)
    print(int(paytax))
tax(int(input()))
